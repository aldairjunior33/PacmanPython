def square(x, y):
    path.up()
    pacman.goto(x, y)
    path.down()
    path.begin_fill()
    
    for count in range(4):
        path.forward(20)
        path.left(90)
    path.end_fill()

def offset(point):
    x = (floor(point.x,20) + 200) /20
    y = (180 - floor(point.y, 20)) /20
    index = int(x + y * 20)
    return index

def valid(point):
    index = offset(point)
    
    if tiles[index] == 0:
        return False
    
    index = offset(point + 19)
    
    if tiles[index] == 0:
        return False
    return point.x % 20 == 0 or point.y % 20 == 0

